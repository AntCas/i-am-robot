interface Env {
	SITES: KVNamespace;
	SESSIONS: KVNamespace;
	ASSETS: Fetcher;
	SIGNING_SECRET: string;
	DEFAULT_SITE_CONFIG?: string;
	DEV_SIGNING_SECRET?: string;
}

type Verdict = "robot" | "human" | "failed";
type SessionStatus = "issued" | "completed" | "expired";
type ChallengeType = "timed_math" | "randomness_audit" | "code_error";

interface SiteConfig {
	siteKey: string;
	secret: string;
	allowedHostnames: string[];
}

interface ChallengeSession {
	id: string;
	siteKey: string;
	hostname: string;
	mode: "prove_robot";
	challengeType: ChallengeType;
	issuedAt: string;
	deadlineAt: string;
	completedAt: string | null;
	status: SessionStatus;
	promptPayload: unknown;
	gradingKey: unknown;
	score: number | null;
	verdict: Verdict | null;
	resultTokenId: string | null;
}

interface ChallengeStartContext {
	siteKey: string;
	hostname: string;
	now: Date;
}

interface ChallengeStartResult {
	promptPayload: unknown;
	gradingKey: unknown;
	timeLimitMs: number;
}

interface ChallengeScoreContext {
	promptPayload: unknown;
	gradingKey: unknown;
	answer: unknown;
	submittedAt: Date;
	deadlineAt: Date;
}

interface ChallengeScoreResult {
	score: number;
	verdict: Verdict;
	reason?: string;
}

interface ChallengeDefinition {
	type: ChallengeType;
	start(ctx: ChallengeStartContext): Promise<ChallengeStartResult>;
	score(ctx: ChallengeScoreContext): Promise<ChallengeScoreResult>;
}

interface StartRequestBody {
	siteKey?: string;
	hostname?: string;
	mode?: string;
}

interface SubmitRequestBody {
	sessionId?: string;
	answer?: unknown;
}

interface VerifyRequestBody {
	secret?: string;
	resultToken?: string;
}

interface ResultTokenPayload {
	tid: string;
	sid: string;
	sk: string;
	host: string;
	ctype: ChallengeType;
	verdict: Verdict;
	score: number;
	iat: number;
	exp: number;
}

const SESSION_TTL_SECONDS = 60 * 15;
const RESULT_TOKEN_TTL_SECONDS = 60 * 5;
const APP_BASE_PATH = "/im-a-robot";
const API_PATH_PREFIX = `${APP_BASE_PATH}/api`;

